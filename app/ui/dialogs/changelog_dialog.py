class ChangelogDialog(QDialog):
    def __init__(self, parent=None) -> None:
        super().__init__(parent)
        self.setWindowTitle("История изменений Velora")
        self.setModal(True)
        self.setMinimumSize(720, 560)

        root = QVBoxLayout(self)
        title = QLabel("ИСТОРИЯ ИЗМЕНЕНИЙ")
        title.setStyleSheet("font-size:16pt; font-weight:600;")
        root.addWidget(title)

        content = QWidget()
        content_layout = QVBoxLayout(content)
        changes = QLabel(CHANGELOG)
        changes.setWordWrap(True)
        changes.setTextInteractionFlags(changes.textInteractionFlags())
        content_layout.addWidget(changes)
        content_layout.addStretch()

        scroll = QScrollArea()
        scroll.setWidgetResizable(True)
        scroll.setFrameShape(QScrollArea.Shape.NoFrame)
        scroll.setWidget(content)
        root.addWidget(scroll, 1)
